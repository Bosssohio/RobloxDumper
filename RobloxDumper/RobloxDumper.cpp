        if (humanoidOffsets.RequiresNeck)     h << "        static constexpr uint32_t RequiresNeck = 0x" << std::hex << humanoidOffsets.RequiresNeck << ";\n";
        if (humanoidOffsets.UseJumpPower)     h << "        static constexpr uint32_t UseJumpPower = 0x" << std::hex << humanoidOffsets.UseJumpPower << ";\n";
