    std::map<uint32_t, uint32_t> containerPairs;
    for (const auto& f : fields) {
        if (f.isContainer && f.containerBegin && f.containerEnd)
            containerPairs[f.containerBegin] = f.containerEnd;
    }
    if (containerPairs.count(0xC8) && containerPairs[0xC8] == 0xD0) {
        jobStart = 0xC8; jobEnd = 0xD0;
    }
    else {
        for (const auto& [begin, end] : containerPairs) {
            if (end - begin == 8) {
                bool hasStride = false;
                for (const auto& f : fields)
                    if (f.offset >= begin && f.offset < end && f.stride != 0) { hasStride = true; break; }
                if (hasStride) { jobStart = begin; jobEnd = end; break; }
            }
        }
    }

    h << "#ifndef OFFSETS_AND_FFLAGS_H\n";
    h << "#define OFFSETS_AND_FFLAGS_H\n\n";
