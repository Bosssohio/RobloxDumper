        if (humanoidOffsets.AutoRotate)       h << "        static constexpr uint32_t AutoRotate = 0x" << std::hex << humanoidOffsets.AutoRotate << ";\n";
        if (humanoidOffsets.Sit)              h << "        static constexpr uint32_t Sit = 0x" << std::hex << humanoidOffsets.Sit << ";\n";
        if (humanoidOffsets.BreakJointsOnDeath) h << "        static constexpr uint32_t BreakJointsOnDeath = 0x" << std::hex << humanoidOffsets.BreakJointsOnDeath << ";\n";
